export class Player {
    id: String
    bpid: String
    firstName: String
    lastName: String
    position: String
    team: String

    constructor(id:String, bpid:String, firstName:String, lastName:String, position:String, team:String) {
        this.id = id
        this.bpid = bpid
        this.firstName = firstName
        this.lastName = lastName
        this.position = position
        this.team = team
    }

}
